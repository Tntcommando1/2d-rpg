using System;
using UnityEngine;

public class pause : MonoBehaviour
{
    bool paused = false;

    bool togglePause()
    {
        if (Time.timeScale == 0f)
        {
            Time.timeScale = 1f;
            return (false);
        }
        else
        {
            Time.timeScale = 0f;
            return (true);
        }
    }
}